#include <unistd.h>

void	putstr(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	while (--argc)
		putstr(argv[argc]);
	return (0);
}
